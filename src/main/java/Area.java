/**
 * @author Lenovo
 *
 */
public class Area
{
	/**
	 * 探索区域的宽度
	 *
	 */
	private final int width;
	/**
	 * 探索区域的长度
	 *
	 */
	private final int length;

	/**
	 * @param width
	 * @param length
	 */
	public Area(final int width, final int length)
	{
		super();
		this.width = width;
		this.length = length;
	}

	/**
	 * @return the width
	 */
	public int getWidth()
	{
		return width;
	}

	/**
	 * @return the length
	 */
	public int getLength()
	{
		return length;
	}

}
